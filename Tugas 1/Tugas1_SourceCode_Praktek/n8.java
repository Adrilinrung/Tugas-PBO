public class n8 {
    public static void main(String[] args) {
        // Deklarasi variabel
        int x = 1;
        int y = 2;
        
        // Menampilkan nilai x dan y
        System.out.print("x = " + x + "\n");
        System.out.print("y = " + y + "\n");
        
        // Menggunakan operator kondisional (ternary operator) untuk memilih nilai yang sesuai
        System.out.print("hasil ekspresi = (x < y) ? x : y = " + ((x < y) ? x : y));
        //Gunakan dalam kurung "(statemen dan kondisi)" untuk menyatakan satu kesatuan pernyataan
    }
}
